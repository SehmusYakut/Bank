    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(isEmptyCustomer()){
            JOptionPane.showMessageDialog(null, "Lütfen boş kutucukları doldurunuz.");
            return;
        }
        String tc = customeTCtext.getText();
        if(trans.CustomerName(tc).equals("Bos")){
                JOptionPane.showMessageDialog(null, "Olmayan şahıs üzerinden güncelleme yapamazsınız.");
        }
        else{
            String name = customerNameText.getText();
            String phone = customerPhoneText.getText();
            String gender = customerGenderText.getText();
            String adres = customerAdressText.getText();
            String id2 = customerConfirmText.getText();
            int id = Integer.parseInt(id2);
            Date date = customerDateChooser.getDate();
            trans.CustomerGuncelle(tc, name, phone, gender, adres, id, date);
            
            customer = trans.customerGetir();
            customerTableGoster();
            accountTableGoster();
            JOptionPane.showMessageDialog(null, "Güncelleme başarılı şekilde gerçekleşti.");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

