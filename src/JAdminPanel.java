    public void accountTableGoster(){
        accountModel.setRowCount(0);
        if(account != null){
            for(Account aa : account){
                Object[] adder = {aa.getTC() ,aa.getType(), aa.getAmount(),aa.getIban()};
                accountModel.addRow(adder);
            }
        }
    }
    
    public void customerTableGoster(){
        customerModel.setRowCount(0);
        if(account != null){
            for(Customer aa : customer){
                Object[] adder = {aa.getName(),aa.getTc(), aa.getPhone(),aa.getAdres(), aa.getDate(), aa.getGender(), aa.getId()};
                customerModel.addRow(adder);
            }
        }
    }
