    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String tc = TCtext.getText();
        if(trans.CustomerName(tc).equals("Bos")){
            JOptionPane.showMessageDialog(null, "Sistemde kaydı olmayan birisine hesap ekleyemezsiniz");
            return;
        }
        String amount = AmountText.getText();
        String type = TypeText.getText();
        String iban = IBANtext.getText();
        if(isEmptyAccount()){
            JOptionPane.showMessageDialog(null, "Lütfen boş kutucukları doldurunuz.");
        }
        else{
            double amount2 = Double.parseDouble(amount);
            trans.addAccount(tc, type, amount2, iban);
            account.add(new Account(type, amount2, iban, tc));
            accountTableGoster();
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

